CREATE TABLE DILER_ORDER(
    ID BIGINT generated by DEFAULT as identity not null primary key,
    COST DOUBLE,
    ORDER_NUMBER BIGINT,
    READY_DATE DATE,
    NAME VARCHAR(255),
    DESCRIPTION VARCHAR(255),
    FK_CREATED_DAILY_SHEET_ID BIGINT,
    FK_WORKED_DAILY_SHEET_ID BIGINT,
    FK_CUSTOMER_ID BIGINT,
    FK_DESIGNER_ID BIGINT,
    STATUS VARCHAR(255) DEFAULT 'design',
    ORDER_ID BIGINT
);



CREATE TABLE DILER_ORDER_DETAIL(
    ID BIGINT generated by DEFAULT as identity not null primary key,
    NUMBER BIGINT,
    NAME VARCHAR(255),
    LENGTH BIGINT,
    WIDTH BIGINT,
    AMOUNT BIGINT,
    ROTATABLE BOOLEAN,
    GLUEING VARCHAR(255),
    MILLING VARCHAR(255),
    GROOVE VARCHAR(255),
    ANGLE45 VARCHAR(255),
    DRILLING VARCHAR(255),
    CUTOFF VARCHAR(255),
    FURNITURE_TYPE_ID BIGINT,
    FURNITURE_CODE_ID BIGINT,
    COMLEX_BOARD_DEF_ID BIGINT,
    SECOND_BOARD_ID BIGINT,
    ORDER_ITEM_ID BIGINT,
    FPRIMARY BOOLEAN,
    DISCRIMINATOR VARCHAR(255) DEFAULT 'OrderFurniture',
    TYPE VARCHAR(255),
    PRICE DOUBLE,
    FURNITURE_ID BIGINT,
    SIZE DOUBLE,
    SIDE VARCHAR(255)
);

CREATE TABLE DILER_ORDER_ITEM(
    ID BIGINT generated by DEFAULT as identity not null primary key,
    NAME VARCHAR(255),
    ORDER_ID BIGINT,
    TYPE VARCHAR(255) DEFAULT 'first', 
    NUMBER BIGINT DEFAULT 1,
    AMOUNT BIGINT DEFAULT 1,
    LENGTH DOUBLE,
    WIDTH DOUBLE,
    DISCRIMINATOR VARCHAR(255) DEFAULT 'OrderItem'
);
